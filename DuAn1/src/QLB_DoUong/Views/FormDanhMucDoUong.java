      private void loaddata(ArrayList<DanhMuc> getlist){
        dtm = (DefaultTableModel) tbldanhmuc.getModel();
        dtm.setRowCount(0);
        for (DanhMuc danhMuc : getlist) {
            dtm.addRow(new Object[]{
                danhMuc.getId(),
                danhMuc.getMaDanhMuc(),
                danhMuc.getTenDanhMuc()
                    
            });
        }
        
    }

    private void tbldanhmucMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbldanhmucMouseClicked
        int row = tbldanhmuc.getSelectedRow();
        if(row ==-1){
            return;
        }
        txtid.setText(tbldanhmuc.getValueAt(row,0).toString());
        txtma.setText(tbldanhmuc.getValueAt(row,1).toString());
        txtten.setText(tbldanhmuc.getValueAt(row,2).toString());
    }//GEN-LAST:event_tbldanhmucMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DanhMuc danhmuc = getformdata();
        String ma= txtma.getText();

        Boolean check = dmsv.check(ma);
        if(check){
            JOptionPane.showMessageDialog(this, "Mã tồn tại");
            return;
        }else if (dmsv.Add(danhmuc)) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loaddata(dmsv.getlist());
            cleaform();
        }else{
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = tbldanhmuc.getSelectedRow();
        if(row == -1){
            return;
        }
        String id = txtid.getText();
        if(dmsv.Delete(id)){
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            loaddata(dmsv.getlist());
            cleaform();
        }else {
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = tbldanhmuc.getSelectedRow();
        DanhMuc danhmuc = getformdata();
        if(row == -1){
            return;
        }
        String id = txtid.getText();
        if(dmsv.Update(id, danhmuc)){
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            loaddata(dmsv.getlist());
            cleaform();
        }else {
            JOptionPane.showMessageDialog(this, "Sửa thất bại");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        ArrayList<DanhMuc> list = dmsv.timkiem(txtma.getText());
        loaddata(list);
        if(list.isEmpty()){
            JOptionPane.showMessageDialog(this, "Không tìm thấy ");
            return ;
        }
    }//GEN-LAST:event_jButton4ActionPerformed


